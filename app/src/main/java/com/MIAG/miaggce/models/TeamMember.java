package com.MIAG.miaggce.models;

public class TeamMember {
    private String name;
    private String function;
    private String number;
    private String number_messenger;
    private String image;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFunction() {
        return function;
    }

    public void setFunction(String function) {
        this.function = function;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getNumber_messenger() {
        return number_messenger;
    }

    public void setNumber_messenger(String number_messenger) {
        this.number_messenger = number_messenger;
    }
}
